package example_09_16_casting;

public class Car {

	public void start() {
		System.out.println("차가 출발합니다.");
	}
	
	public void stop() {
		System.out.println("차가 정지합니다.");
		
	}
	
	public void drive() {
		System.out.println("차를 운전합니다.");
		
	}

	public void speedUp() {
		System.out.println("속도를 올립니다.");
		
	}

	public void speedDown() {
		System.out.println("속도를 내립니다.");
		
	}

}
