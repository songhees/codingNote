package stringArray;

import java.io.IOException;

public class StringArray01 {
	public static void main(String[] args) throws IOException {
		int b = System.in.read();
		System.out.println(b);

		System.in.read();
		System.in.read();
		
		char c = (char) System.in.read();
		System.out.println(c);
	}
}
